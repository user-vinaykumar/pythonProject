# here we are going to sort the list in reverse order, the original list is going to be
# altered/overrided

courses = ['Art', 'Chemistry', 'Polity', 'Biology', 'CompSci']

courses.sort()  # sorts in asc / alphabetical order
print(courses)


courses.sort(reverse=True)
print(courses)

nums = [4,7,2,1,8,3]

nums.sort() # sorts in asc / alphabetical order
print(nums)

nums.sort(reverse=True) # sorts in desc order
print(nums)